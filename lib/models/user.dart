class User {
  final String id;
  final String name;
  final String email;
  final int height;
  final double weight;
  final int age;
  final int caloriesGoal;

  User({
    required this.id,
    required this.name,
    required this.email,
    required this.height,
    required this.weight,
    required this.age,
    required this.caloriesGoal,
  });
}
